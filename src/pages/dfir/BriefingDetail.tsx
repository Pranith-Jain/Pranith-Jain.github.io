/**
 * Wire-level severity as it can arrive from the briefing API — includes the
 * legacy `'unknown'` bucket that the canonical ramp folds into `low`.
 */
type Severity = 'critical' | 'high' | 'medium' | 'low' | 'unknown';

/** Map a wire severity onto the canonical ramp (unknown → low). */
function canonSeverity(s: Severity): CanonSeverity {
  return s === 'unknown' ? 'low' : s;
}

/** Short uppercase label per (wire) severity for the finding chip. */
const SEVERITY_LABEL: Record<Severity, string> = {
  critical: 'CRIT',
  high: 'HIGH',
  medium: 'MED',
  low: 'LOW',
  unknown: 'N/A',
};

/** Severity-coloured focus ring, aligned to the canonical ramp. */
const SEVERITY_RING: Record<CanonSeverity, string> = {
  critical: 'ring-rose-500/40',
  high: 'ring-orange-500/40',
  medium: 'ring-amber-500/40',
  low: 'ring-slate-400/40',
  info: 'ring-sky-500/40',
};

