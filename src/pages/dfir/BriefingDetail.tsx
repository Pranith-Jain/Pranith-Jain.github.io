export default function BriefingDetail(): JSX.Element {
  const { slug } = useParams<{ slug: string }>();
  const [briefing, setBriefing] = useState<Briefing | null>(null);
  const [error, setError] = useState<string | null>(null);
  const [loading, setLoading] = useState(true);
  const [refreshKey, setRefreshKey] = useState(0);

  useEffect(() => {
    if (!slug) return;
    let cancelled = false;
    const ctrl = new AbortController();
    setLoading(true);
    setError(null);
    setBriefing(null);
    fetch(`/api/v1/briefings/${encodeURIComponent(slug)}`, {
      signal: AbortSignal.any([ctrl.signal, AbortSignal.timeout(15_000)]),
    })
      .then(async (r) => {
        if (!r.ok) {
          const body = await r.text().catch(() => '');
          let msg = `${r.status}`;
          try {
            const parsed = JSON.parse(body) as { error?: string };
            msg = parsed.error ?? msg;
          } catch (_catchErr) {
            console.error('BriefingDetail failed:', _catchErr instanceof Error ? _catchErr.message : String(_catchErr));
            /* use default */
          }
          throw new Error(msg);
        }
        const ct = r.headers.get('content-type') ?? '';
        if (!ct.includes('json')) throw new Error('Server returned non-JSON response');
        return (await r.json()) as Briefing;
      })
      .then((b) => {
        if (!cancelled) setBriefing(b);
      })
      .catch((err: { name?: string; message?: string }) => {
        if (cancelled || err.name === 'AbortError') return;
        setError(err.message ?? 'failed to load');
      })
      .finally(() => {
        if (!cancelled) setLoading(false);
      });
    return () => {
      cancelled = true;
      ctrl.abort();
    };
  }, [slug, refreshKey]);

  // Concatenate the briefing's narrative content into one body string the
  // intel extractor can scan — executive summary + every finding's title +
  // description. The flat-IoC tables further down stay as the human-friendly
  // browsing surface; the card adds the structured STIX 2.1 view on top.
  // Called before the conditional returns so it satisfies the rules-of-hooks
  // ordering invariant across renders.
  const intelBody = useMemo(() => {
    if (!briefing) return '';
    const parts: string[] = [briefing.executive_summary];
    // Landscape reports have a different structure — use named sections.
    if (briefing.type === 'landscape') {
      const lr = briefing as unknown as LandscapeReport;
      for (const section of [
        lr.top_threats,
        lr.trending_actors,
        lr.key_incidents,
        lr.recommended_actions,
        lr.outlook,
      ]) {
        parts.push(`\n\n## ${section.title}\n${section.blurb}`);
        for (const f of section.findings) {
          parts.push(`\n### ${f.title}\n${f.description}`);
        }
      }
    } else {
      for (const s of briefing.sections) {
        parts.push(`\n\n## ${s.title}\n${s.blurb}`);
        for (const f of s.findings) {
          parts.push(`\n### ${f.title}\n${f.description}`);
        }
      }
    }
    return parts.join('\n');
  }, [briefing]);

  if (loading) {
    return (
      <div className="max-w-5xl mx-auto px-4 sm:px-8 py-12 sm:py-16 font-mono text-sm text-slate-500">
        Loading briefing…
      </div>
    );
  }
  if (error || !briefing) {
    return (
      <div className="max-w-5xl mx-auto px-4 sm:px-8 py-12 sm:py-16">
        <BackLink to="/threatintel">back</BackLink>
        <h1 className="font-display font-bold text-2xl text-slate-900 dark:text-white mb-2">Briefing not found</h1>
        <p className="text-sm text-slate-500">
          {error ??
            'This briefing has not been generated yet. Daily briefings publish at 00:05 UTC; weekly at 00:15 UTC Monday.'}
        </p>
        <button
          type="button"
          onClick={() => setRefreshKey((k) => k + 1)}
          className="mt-4 inline-flex items-center gap-1 text-meta font-mono px-2.5 py-1.5 rounded border border-slate-300 dark:border-[rgb(var(--border-400))] hover:border-brand-500/40"
        >
          retry
        </button>
      </div>
    );
  }

  const stats = briefing.stats;
  const shareUrl = typeof window !== 'undefined' ? window.location.href : '';
  const shareTitle = briefing.title;
  return (
    <div className="max-w-5xl mx-auto px-4 sm:px-8 py-12 text-slate-900 dark:text-white">
      <BackLink to="/threatintel">back</BackLink>

      <header className="animate-fade-in-up mb-8">
        <span className="inline-block text-eyebrow font-mono uppercase tracking-[0.2em] text-slate-500 dark:text-slate-400 mb-3">
          Intel Briefing · {briefing.type}
        </span>
        <h1 className="text-3xl sm:text-4xl font-display font-bold leading-tight mb-2">{briefing.title}</h1>
        <p className="text-sm font-mono text-slate-500 mb-3">
          {briefing.date_range} · generated {briefing.generated_at.slice(0, 16).replace('T', ' ')} UTC
        </p>
        <AutoGeneratedBadge kind="briefing" generatedAt={briefing.generated_at} variant="banner" />
        {briefing.degraded && (
          <div
            role="alert"
            className="mt-4 rounded-xl border border-amber-300 dark:border-amber-800/60 bg-amber-50 dark:bg-amber-900/15 p-4"
          >
            <div className="flex items-start gap-3">
              <AlertTriangle
                size={18}
                className="text-amber-700 dark:text-amber-400 shrink-0 mt-0.5"
                aria-hidden="true"
              />
              <div className="min-w-0 flex-1">
                <p className="text-sm font-semibold text-amber-900 dark:text-amber-200">
                  Upstream feeds were unreachable when this briefing was built.
                </p>
                <p className="text-xs text-amber-800 dark:text-amber-300 mt-1 leading-relaxed">
                  CISA KEV and NVD returned errors from the edge at build time. The hourly self-heal will rebuild this
                  briefing as soon as the feeds respond — within an hour of recovery. This is{' '}
                  <span className="font-semibold">not</span> an all-clear; do not treat zero findings as &quot;no new
                  vulnerabilities&quot;.
                </p>
                <button
                  type="button"
                  onClick={() => setRefreshKey((k) => k + 1)}
                  className="mt-3 inline-flex items-center gap-1.5 text-xs font-mono px-2.5 py-1.5 rounded border border-amber-400/60 text-amber-900 dark:text-amber-200 hover:bg-amber-500/10"
                >
                  retry now
                </button>
              </div>
            </div>
          </div>
        )}
        <a
          href={`/api/v1/briefings/${briefing.slug}/print`}
          target="_blank"
          className="mt-3 inline-flex items-center gap-1.5 text-xs font-mono px-3 py-1.5 rounded-xl border border-slate-300 dark:border-[rgb(var(--border-400))] hover:border-brand-500/40 hover:bg-brand-500/5 transition-colors"
        >
          <svg
            width="14"
            height="14"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth="2"
            strokeLinecap="round"
            strokeLinejoin="round"
          >
            <polyline points="6 9 6 2 18 2 18 9" />
            <path d="M6 18H4a2 2 0 0 1-2-2v-5a2 2 0 0 1 2-2h16a2 2 0 0 1 2 2v5a2 2 0 0 1-2 2h-2" />
            <rect x="6" y="14" width="12" height="8" />
          </svg>
          Print / PDF
        </a>
        <div className="mt-3 flex items-center gap-2">
          <a
            href={`https://x.com/intent/tweet?text=${encodeURIComponent(shareTitle)}&url=${encodeURIComponent(shareUrl)}`}
            target="_blank"
            rel="noopener noreferrer"
            className="inline-flex items-center gap-1.5 text-xs font-mono px-3 py-1.5 rounded-xl border border-slate-300 dark:border-[rgb(var(--border-400))] hover:border-brand-500/40 hover:bg-brand-500/5 transition-colors"
          >
            <svg viewBox="0 0 24 24" className="h-3.5 w-3.5" fill="currentColor" aria-hidden="true">
              <path d="M18.244 2.25h3.308l-7.227 8.26 8.502 11.24H16.17l-5.214-6.817L4.99 21.75H1.68l7.73-8.835L1.254 2.25H8.08l4.713 6.231zm-1.161 17.52h1.833L7.084 4.126H5.117z" />
            </svg>
            X
          </a>
          <a
            href={`https://www.linkedin.com/sharing/share-offsite/?url=${encodeURIComponent(shareUrl)}`}
            target="_blank"
            rel="noopener noreferrer"
            className="inline-flex items-center gap-1.5 text-xs font-mono px-3 py-1.5 rounded-xl border border-slate-300 dark:border-[rgb(var(--border-400))] hover:border-brand-500/40 hover:bg-brand-500/5 transition-colors"
          >
            <svg viewBox="0 0 24 24" className="h-3.5 w-3.5" fill="currentColor" aria-hidden="true">
              <path d="M20.447 20.452h-3.554v-5.569c0-1.328-.027-3.037-1.852-3.037-1.853 0-2.136 1.445-2.136 2.939v5.667H9.351V9h3.414v1.561h.046c.477-.9 1.637-1.85 3.37-1.85 3.601 0 4.267 2.37 4.267 5.455v6.286zM5.337 7.433a2.062 2.062 0 01-2.063-2.065 2.064 2.064 0 112.063 2.065zm1.782 13.019H3.555V9h3.564v11.452zM22.225 0H1.771C.792 0 0 .774 0 1.729v20.542C0 23.227.792 24 1.771 24h20.451C23.2 24 24 23.227 24 22.271V1.729C24 .774 23.2 0 22.222 0h.003z" />
            </svg>
            LinkedIn
          </a>
        </div>
      </header>

      {/* Landscape reports have their own section layout — branch before the
          daily/weekly findings/IOC tables render. */}
      {briefing.type === 'landscape' && <LandscapeReportView briefing={briefing as unknown as LandscapeReport} />}

      {/* Stats bar — daily/weekly only; landscape has its own stats in LandscapeReportView. */}
      {briefing.type !== 'landscape' && (
        <section className="surface-card p-4 mb-8">
          <div className="flex flex-wrap items-center justify-around gap-6">
            <StatPill label="findings" value={stats.findings} />
            <StatPill label="sections" value={stats.sections} />
            <StatPill label="CVEs" value={stats.cves} />
            <StatPill label="KEVs" value={stats.kevs} />
            <StatPill label="IOCs" value={stats.iocs} />
            <StatPill label="critical" value={stats.critical} accent="text-rose-600 dark:text-rose-400" />
            <StatPill label="high" value={stats.high} accent="text-orange-600 dark:text-orange-400" />
            <StatPill label="medium" value={stats.medium} accent="text-amber-600 dark:text-amber-400" />
            <StatPill label="low" value={stats.low} accent="text-slate-600 dark:text-slate-300" />
          </div>
        </section>
      )}

      {/* Jump-nav pills — link to the CVE / ransomware / IOC / MITRE blocks
          further down the page. Hidden on landscape reports and on briefs
          that have nothing in any of those buckets. */}
      {briefing.type !== 'landscape' && (
        <JumpNav
          cveCount={stats.cves}
          ransomwareCount={stats.ransomware_victims}
          iocCount={stats.iocs}
          mitreCount={briefing.mitre_techniques?.length ?? 0}
        />
      )}

      {/* Structured STIX 2.1 intel view. Renders an enriched card backed by
          /api/v1/intel-bundle — heuristic-extracted actors, malware, CVEs,
          theme tags + bulk-enriched IoCs with risk scores. Falls back to
          rendering nothing while loading so the Executive Summary still
          carries the page on first paint. */}
      <section className="mb-10">
        <IntelCard
          sourceId="briefings"
          itemRef={briefing.slug}
          item={{
            title: briefing.title,
            body: intelBody,
            publishedAt: briefing.generated_at,
          }}
          fallback={null}
        />
      </section>

      {/* Executive Summary */}
      <section className="mb-10">
        <h2 className="font-display font-bold text-lg mb-3 flex items-center gap-2">
          <ShieldAlert size={18} className="text-brand-600 dark:text-brand-400" /> Executive Summary
        </h2>
        <p className="text-sm text-slate-700 dark:text-slate-300 leading-relaxed">{briefing.executive_summary}</p>
      </section>

      {/* Sections — daily/weekly only; landscape uses LandscapeReportView above.
          The id="briefing-cves" target is used by the JumpNav pills. */}
      {briefing.type !== 'landscape' && briefing.sections && briefing.sections.length > 0 && (
        <section id="briefing-cves" className="mb-12 space-y-8 scroll-mt-20">
          {briefing.sections.map((s) => (
            <div
              key={s.id}
              id={s.id === 'ransomware-activity' ? 'briefing-ransomware' : undefined}
              className="scroll-mt-20"
            >
              <div className="flex items-baseline justify-between mb-2">
                <h2 className="font-display font-bold text-xl flex items-center gap-2">
                  <AlertTriangle size={18} className="text-brand-600 dark:text-brand-400" />
                  {s.title}
                </h2>
                <span className="text-xs font-mono text-slate-500">
                  {s.count} {s.count === 1 ? 'finding' : 'findings'}
                </span>
              </div>
              <p className="text-sm text-muted mb-4">{s.blurb}</p>
              <div className="space-y-3">
                {s.findings.map((f) => (
                  <FindingCard key={f.id} finding={f} />
                ))}
              </div>
            </div>
          ))}
        </section>
      )}

      {/* No sections message — when there are no findings but IOCs exist */}
      {briefing.type !== 'landscape' &&
        briefing.sections.length === 0 &&
        briefing.ioc_dump &&
        briefing.ioc_dump.count > 0 && (
          <section className="mb-8 surface-card p-5">
            <div className="flex items-start gap-3">
              <AlertTriangle size={18} className="text-amber-500 mt-0.5 shrink-0" />
              <div>
                <p className="text-sm font-semibold text-slate-900 dark:text-white mb-1">
                  No new critical/high CVEs in this period
                </p>
                <p className="text-xs text-slate-500 dark:text-slate-400 leading-relaxed">
                  The upstream feeds (CISA KEV, NVD) had no new high/critical vulnerabilities for this date range. The
                  IOC indicators below were still collected from URLhaus, MalwareBazaar, ThreatFox, and TweetFeed.
                </p>
              </div>
            </div>
          </section>
        )}

      {/* MITRE techniques observed — daily/weekly only. id="briefing-mitre" is
          the JumpNav target. */}
      {briefing.type !== 'landscape' && briefing.mitre_techniques && briefing.mitre_techniques.length > 0 && (
        <section id="briefing-mitre" className="mb-10 scroll-mt-20">
          <h2 className="font-display font-bold text-lg mb-3">MITRE ATT&amp;CK Techniques Observed</h2>
          <div className="flex flex-wrap gap-2">
            {briefing.mitre_techniques.map((t) => (
              <MitreChip key={t} technique={t} />
            ))}
          </div>
        </section>
      )}

      {/* IOCs — daily/weekly only. Rendered as a single txt dump (capped at
          30 total) with download + copy buttons, NOT inline tables. The inline
          per-kind IocTable is gone — when a user wants the actual list of
          indicators they grab the txt and use it as a blocklist seed.
          id="briefing-iocs" is the JumpNav target. */}
      {briefing.type !== 'landscape' && briefing.ioc_dump && briefing.ioc_dump.count > 0 && (
        <section id="briefing-iocs" className="mb-10 scroll-mt-20">
          <div className="mb-4 flex items-baseline justify-between gap-4">
            <h2 className="font-display font-bold text-lg">Active Threat Indicators</h2>
            <span className="text-xs font-mono text-slate-500">
              {briefing.ioc_dump.count.toLocaleString()} unique indicators
            </span>
          </div>
          <IocDumpPanel slug={briefing.slug} dump={briefing.ioc_dump} />
        </section>
      )}

      {/* Footer */}
      <footer className="mt-12 pt-6 border-t border-slate-200 dark:border-[rgb(var(--border-400))] text-xs font-mono text-slate-500">
        <p>
          Sources: {briefing.sources.join(', ') || 'none'}. Reference only. Verify all indicators in your own
          environment. Generated {briefing.generated_at.slice(0, 16).replace('T', ' ')} UTC.
        </p>
      </footer>
    </div>
  );
}
