export type Severity = 'critical' | 'high' | 'medium' | 'low' | 'info';

export interface ScannerHit {
  severity: Severity;
  ruleId: string;
  kind: string;
  message: string;
  line: number;
  column: number;
  snippet: string;
  recommendation: string;
}

export interface ScannerRule {
  id: string;
  name: string;
  kind: string;
  severity: Severity;
  languages: string[];
  pattern: RegExp;
  message: string;
  recommendation: string;
  cwe?: string;
}

const SEVERITY_ORDER: Record<Severity, number> = {
  critical: 0,
  high: 1,
  medium: 2,
  low: 3,
  info: 4,
};

export function severityOrder(s: Severity): number {
  return SEVERITY_ORDER[s];
}

