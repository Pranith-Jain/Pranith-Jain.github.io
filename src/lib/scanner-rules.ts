export function detectLanguage(filename: string, content: string): string {
  const ext = filename.split('.').pop()?.toLowerCase() || '';
  const firstLine = content.split('\n')[0]?.trim() || '';

  const extMap: Record<string, string> = {
    py: 'python',
    js: 'javascript',
    jsx: 'javascript',
    ts: 'typescript',
    tsx: 'typescript',
    go: 'go',
    rs: 'rust',
    php: 'php',
    tf: 'terraform',
    yaml: 'kubernetes',
    yml: 'kubernetes',
    dockerfile: 'dockerfile',
    md: 'markdown',
    dockerignore: 'dockerfile',
  };

  if (extMap[ext]) return extMap[ext];
  if (firstLine.match(/^#!/)) {
    if (/python/i.test(firstLine)) return 'python';
    if (/node/i.test(firstLine)) return 'javascript';
    if (/bash|sh/i.test(firstLine)) return 'text';
  }
  if (/Dockerfile/i.test(filename)) return 'dockerfile';

  return 'text';
}
