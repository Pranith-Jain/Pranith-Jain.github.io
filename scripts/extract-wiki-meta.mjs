#!/usr/bin/env node
/**
 * Generate src/data/dfir/wiki-meta.ts from src/data/dfir/wiki-articles.ts.
 *
 * Why this exists:
 *   wiki-articles.ts holds full article bodies (~109KB total). Wiki.tsx (the
 *   list page) only needs slug/title/category/description, so importing the
 *   full module there would force every list-page visitor to download the
 *   bodies they're not reading. wiki-meta.ts holds just the slim metadata —
 *   the list page imports from here.
 *
 * Drift guard:
 *   This script runs as a `prebuild` npm hook so the meta file is
 *   regenerated on every build. If a future commit edits an article's
 *   title/description in wiki-articles.ts, the next build picks it up
 *   automatically.
 *
 * Usage:
 *   node scripts/extract-wiki-meta.mjs
 *   npm run wiki:meta              # alias
 *   npm run build                  # runs this implicitly via prebuild
 */

import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SOURCE = resolve(__dirname, '..', 'src/data/dfir/wiki-articles.ts');
const OUTPUT = resolve(__dirname, '..', 'src/data/dfir/wiki-meta.ts');

const QUOTED = String.raw`(?:'(?:\\.|[^'\\])*'|"(?:\\.|[^"\\])*")`;
const ARTICLE_RE = new RegExp(
  String.raw`\{\s*slug:\s*'([^']+)',\s*title:\s*(${QUOTED}),\s*category:\s*'([^']+)',\s*description:\s*\n?\s*(${QUOTED})`,
  'g',
);

function unquote(s) {
  const q = s[0];
  const inner = s.slice(1, -1);
  if (q === "'") return inner.replace(/\\'/g, "'").replace(/\\\\/g, '\\');
  return inner.replace(/\\"/g, '"').replace(/\\\\/g, '\\');
}

async function main() {
  const src = await readFile(SOURCE, 'utf8');
  const entries = [];
  let m;
  while ((m = ARTICLE_RE.exec(src)) !== null) {
    entries.push({
      slug: m[1],
      title: unquote(m[2]),
      category: m[3],
      description: unquote(m[4]),
    });
  }

  // Sanity check — every `slug:` in source must appear in our extracted set.
  // The regex misses any article whose fields are reordered or whose body
  // appears before description; failing fast tells us to fix the regex
  // instead of silently shipping incomplete meta.
  const allSlugs = [...src.matchAll(/^\s*slug:\s*'([^']+)'/gm)].map((x) => x[1]);
  const captured = new Set(entries.map((e) => e.slug));
  const missing = allSlugs.filter((s) => !captured.has(s));
  if (missing.length > 0) {
    console.error(
      `extract-wiki-meta: regex missed ${missing.length} article(s) — fix the parser:\n  ${missing.join('\n  ')}`,
    );
    process.exit(1);
  }

  const lines = [
    '/**',
    ' * Auto-generated by scripts/extract-wiki-meta.mjs — DO NOT EDIT BY HAND.',
    ' * Source of truth is wiki-articles.ts; this file is regenerated on every',
    ' * `npm run build` via the `prebuild` hook. Edits here are overwritten.',
    ' *',
    ' * Wiki.tsx (list page) imports from here so it does NOT pay the ~109KB',
    ' * cost of the full bodies. WikiArticle.tsx loads bodies dynamically.',
    ' */',
    '',
    "import type { WikiCategory } from './wiki-articles';",
    'export type { WikiCategory };',
    '',
    'export interface WikiArticleMeta {',
    '  slug: string;',
    '  title: string;',
    '  category: WikiCategory;',
    '  description: string;',
    '}',
    '',
    'export const wikiMeta: WikiArticleMeta[] = [',
  ];
  for (const e of entries) {
    lines.push(
      `  { slug: ${JSON.stringify(e.slug)}, title: ${JSON.stringify(e.title)}, category: ${JSON.stringify(e.category)}, description: ${JSON.stringify(e.description)} },`,
    );
  }
  lines.push('];', '');

  await writeFile(OUTPUT, lines.join('\n'), 'utf8');
  console.log(`extract-wiki-meta: wrote ${entries.length} articles to ${OUTPUT}`);
}

void main();
