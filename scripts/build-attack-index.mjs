/**
 * Regenerate api/src/data/attack-id-index.ts — maps every MITRE ATT&CK
 * external id (T1566.001, S0001, G0016, M1049, TA0001, DS0009, C0001…)
 * to its STIX 2.1 object id + the TAXII collection that holds it.
 *
 * The public MITRE TAXII server (attack-taxii.mitre.org) does NOT support
 * match[external_id]; only match[id]. So stix-fetch resolves an ATT&CK id
 * → STIX uuid via this committed index, then fetches by uuid (supported).
 *
 * Run periodically + commit (ATT&CK revises ~quarterly):
 *   node scripts/build-attack-index.mjs
 */
import { writeFileSync, mkdirSync } from 'node:fs';

const DOMAINS = [
  ['e', 'https://raw.githubusercontent.com/mitre/cti/master/enterprise-attack/enterprise-attack.json'],
  ['i', 'https://raw.githubusercontent.com/mitre/cti/master/ics-attack/ics-attack.json'],
  ['m', 'https://raw.githubusercontent.com/mitre/cti/master/mobile-attack/mobile-attack.json'],
];
const OUT_DIR = new URL('../api/src/data/', import.meta.url);
const OUT = new URL('../api/src/data/attack-id-index.ts', import.meta.url);

async function main() {
  const index = {};
  let total = 0;
  for (const [col, url] of DOMAINS) {
    process.stdout.write(`Fetching ${url.split('/').slice(-1)[0]}…\n`);
    const res = await fetch(url, { headers: { 'user-agent': 'pranithjain build-attack-index' } });
    if (!res.ok) throw new Error(`${url} ${res.status}`);
    const { objects } = await res.json();
    for (const o of objects) {
      if (o.revoked || o.x_mitre_deprecated || typeof o.id !== 'string') continue;
      const ref = (o.external_references ?? []).find(
        (r) => r.source_name === 'mitre-attack' && typeof r.external_id === 'string'
      );
      if (!ref) continue;
      // First domain that defines an id wins (enterprise is the canonical one).
      if (!index[ref.external_id]) {
        const entry = { id: o.id, col };
        // Capture the technique's tactic (kill-chain phase) for Attack-Flow
        // ordering. Only attack-pattern objects carry kill_chain_phases.
        if (o.type === 'attack-pattern' && Array.isArray(o.kill_chain_phases)) {
          const ph = o.kill_chain_phases.find(
            (p) => p.kill_chain_name === 'mitre-attack' && typeof p.phase_name === 'string'
          );
          if (ph) entry.tac = ph.phase_name;
        }
        index[ref.external_id] = entry;
        total++;
      }
    }
  }

  mkdirSync(OUT_DIR, { recursive: true });
  const header = `/**
 * AUTO-GENERATED by scripts/build-attack-index.mjs from the MITRE ATT&CK
 * enterprise/ics/mobile STIX bundles. Do not edit by hand.
 * Generated: ${new Date().toISOString()} · ${total} ATT&CK ids.
 *
 * col: 'e' Enterprise · 'i' ICS · 'm' Mobile (TAXII collection selector).
 * tac: technique's MITRE ATT&CK tactic (kill-chain phase_name), techniques only.
 */
export interface AttackRef { id: string; col: 'e' | 'i' | 'm'; tac?: string }
export const ATTACK_ID_INDEX: Record<string, AttackRef> = `;
  writeFileSync(OUT, header + JSON.stringify(index) + ';\n');
  process.stdout.write(`Wrote ${total} ids → api/src/data/attack-id-index.ts (${(JSON.stringify(index).length / 1024).toFixed(0)} KB)\n`);
}

main().catch((e) => {
  process.stderr.write(`build-attack-index failed: ${e.message}\n`);
  process.exit(1);
});
