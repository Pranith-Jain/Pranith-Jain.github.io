function computeSizeBytes(obj) {
  return Buffer.byteLength(JSON.stringify(obj), 'utf8');
}

function main() {
  ensureDir(OUT);

  const entries = portals.map((p) => ({
    ...p,
    sizeBytes: computeSizeBytes(p),
  }));

  const index = {
    source: SOURCE,
    license: LICENSE,
    replicatedAt: new Date().toISOString().slice(0, 10),
    count: entries.length,
    entries,
  };

  writeFileSync(join(OUT, 'index.json'), JSON.stringify(index, null, 2));

  console.log(`✔ Built OSINT portal manifest:`);
  console.log(`    ${entries.length} portals`);
  console.log(`    Categories: ${[...new Set(entries.map((p) => p.category))].join(', ')}`);
}

main();
