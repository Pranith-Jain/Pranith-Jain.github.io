function normalizeMac(raw: string): string {
  return raw
    .replace(/[:-]/g, '.')
    .split('.')
    .map((o) => o.toUpperCase())
    .join(':');
}

function isValidMac(raw: string): boolean {
  return MAC_RE.test(raw);
}

function cacheGet(key: string): Promise<Response | undefined> {
  return caches.default.match(new Request(key));
}

function cachePut(key: string, res: Response): Promise<void> {
  return caches.default.put(new Request(key), res.clone()).then(() => {});
}

export async function wifiInvestigationHandler(c: Context<{ Bindings: Env }>): Promise<Response> {
  const bssid = c.req.query('bssid')?.trim();
  const ssid = c.req.query('ssid')?.trim();

  if (!bssid && !ssid) {
    return c.json({ error: 'Either bssid or ssid query parameter is required' }, 400);
  }

  if (bssid && ssid) {
    return c.json({ error: 'Provide either bssid or ssid, not both' }, 400);
  }

  if (bssid && !isValidMac(bssid)) {
    return c.json(
      { error: 'Invalid MAC address format. Expected AA:BB:CC:DD:EE:FF, AA-BB-CC-DD-EE-FF, or AA.BB.CC.DD.EE.FF' },
      400
    );
  }

  const cacheKey = bssid
    ? `https://wifi-investigate.internal/bssid-${encodeURIComponent(bssid)}`
    : `https://wifi-investigate.internal/ssid-${encodeURIComponent(ssid!)}`;

  const cached = await cacheGet(cacheKey);
  if (cached) return cached;

  if (bssid) {
    return handleBssidLookup(c, bssid, cacheKey);
  }

  return handleSsidLookup(c, ssid!, cacheKey);
}

async function handleBssidLookup(c: Context<{ Bindings: Env }>, rawBssid: string, cacheKey: string): Promise<Response> {
  const formatted = normalizeMac(rawBssid);
  const oui = formatted.slice(0, 8);
  const vendor = OUI_DB.get(oui) || 'Unknown';
  const firstOctet = parseInt(formatted.slice(0, 2), 16);
  const isLocallyAdministered = (firstOctet & 0x02) !== 0;
  const isMulticast = (firstOctet & 0x01) !== 0;

  const lookups = [
    {
      service: 'WiGLE.net',
      url: `https://wigle.net/search?netid=${formatted}`,
      description: 'Wireless network mapping database',
    },
    {
      service: 'Google Search',
      url: `https://www.google.com/search?q=${encodeURIComponent(`"${formatted}" wifi`)}&num=10`,
      description: 'Web search for BSSID references',
    },
    { service: 'MacVendors.com', url: `https://macvendors.com/${formatted}`, description: 'MAC address vendor lookup' },
    {
      service: 'Wi-Fi Analyzer',
      url: `https://www.wi-fi.org/discover-wi-fi/search?q=${formatted}`,
      description: 'Wi-Fi Alliance device search',
    },
  ];

  const flags: string[] = [];

  if (isLocallyAdministered) {
    flags.push('Locally administered MAC — likely randomized/spoofed, not a real device OUI');
  }

  if (isMulticast) {
    flags.push('Multicast address — used for group traffic, not a single device');
  }

  if (vendor === 'Unknown') {
    flags.push('OUI not in vendor database — may be locally administered or from an unmapped manufacturer');
  }

  const result = {
    query: { type: 'bssid', value: rawBssid },
    mac: {
      formatted,
      oui,
      vendor,
      is_locally_administered: isLocallyAdministered,
      is_multicast: isMulticast,
    },
    lookups,
    flags,
  };

  const raw = JSON.stringify(result);
  const res = new Response(raw, {
    status: 200,
    headers: {
      'content-type': 'application/json',
      'cache-control': `public, max-age=${CACHE_TTL_SECONDS}`,
    },
  });
  c.executionCtx.waitUntil(cachePut(cacheKey, res));
  return res;
}

async function handleSsidLookup(c: Context<{ Bindings: Env }>, ssid: string, cacheKey: string): Promise<Response> {
  const lookups = [
    {
      service: 'WiGLE.net',
      url: `https://wigle.net/search?ssid=${encodeURIComponent(ssid)}`,
      description: 'Wireless network mapping database',
    },
    {
      service: 'Google Search',
      url: `https://www.google.com/search?q=${encodeURIComponent(`"${ssid}" wifi network`)}&num=10`,
      description: 'Web search for SSID references',
    },
    {
      service: 'Wi-Fi Alliance',
      url: `https://www.wi-fi.org/discover-wi-fi/search?q=${encodeURIComponent(ssid)}`,
      description: 'Wi-Fi Alliance device search',
    },
  ];

  const flags: string[] = [];

  for (const [pattern, flag] of DEFAULT_SSID_PATTERNS) {
    if (pattern.test(ssid)) {
      flags.push(flag);
      break;
    }
  }

  if (flags.length === 0) {
    flags.push('Non-default SSID — network has likely been customized by the administrator');
  }

  const result = {
    query: { type: 'ssid', value: ssid },
    lookups,
    flags,
  };

  const raw = JSON.stringify(result);
  const res = new Response(raw, {
    status: 200,
    headers: {
      'content-type': 'application/json',
      'cache-control': `public, max-age=${CACHE_TTL_SECONDS}`,
    },
  });
  c.executionCtx.waitUntil(cachePut(cacheKey, res));
  return res;
}
